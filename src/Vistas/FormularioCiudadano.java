     public boolean verificarCorreo (String correo){
    // se compara con la expresion regular
        Pattern patron = Pattern.compile("^[_A-Za-z0-9-\\+]+(\\.[_A-Za-z0-9-]+)*@"
                                    + "[A-Za-z0-9-]+(\\.[A-Za-z0-9]+)*(\\.[A-Za-z]{2,})$");
        Matcher mat= patron.matcher(correo);
        return mat.find();
    }
  
    
